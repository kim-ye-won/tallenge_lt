package com.example.tallenge_lt;

public class ChooseExpData {
    private String iv_ctf;
    private String tv_exp;


    public ChooseExpData(String iv_ctf, String tv_exp) {
        this.iv_ctf = iv_ctf;
        this.tv_exp = tv_exp;
    }


    public String getIv_ctf() {
        return iv_ctf;
    }

    public void setIv_ctf(String iv_ctf) {
        this.iv_ctf = iv_ctf;
    }

    public String getTv_exp() {
        return tv_exp;
    }

    public void setTv_exp(String tv_exp) {
        this.tv_exp = tv_exp;
    }
}
